#pragma once

#include <windows.h>

class GameTimer
{
public:
	GameTimer();

	void Reset(); // タイマーの初期化
	void Tick(); // マイフレーム呼ぶ（時間の更新）

	float DeltaTime() const; // 1フレームの時間（秒）を返す

private:
	double mSecondsPerCount;
	double mDeltaTime;

	__int64 mBaseTime;
	__int64 mPausedTime;
	__int64 mStopTime;
	__int64 mPrevTime;
	__int64 mCurrTime;

	bool mStopped;

};